public class AdresseMultiCast {

    private String name;
    private String Adresse;

    public AdresseMultiCast(String name,String Adresse){
        this.name = name;
        this.Adresse = Adresse;
    }

    public String getAdresse() {
        return Adresse;
    }

    public String getName() {
        return name;
    }
}
